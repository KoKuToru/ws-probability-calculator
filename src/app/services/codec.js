function *findCodePoints() {
  const params = new URLSearchParams();
  for (let i = 0; i < 0x10FFFF; ++i) {
    params.set('', String.fromCodePoint(i));
    if (params.toString().endsWith(params.get(''))) {
      yield params.get('');
    }
  }
}
